'use strict';

module.exports = function(gulp, config) {
    var OPTIONS = config;

    ////////////////////////////////////////////////////////////////////
    // COPY FONTS
    ////////////////////////////////////////////////////////////////////
    gulp.task('build:scripts:copy', function() {
        return gulp.src(OPTIONS.GLOB.JS)
            .pipe(gulp.dest(OPTIONS.DIR.BUILD_SCRIPTS));
    });

    ////////////////////////////////////////////////////////////////////
    // TASKS
    ////////////////////////////////////////////////////////////////////

    gulp.task('build:scripts', ['build:scripts:copy']);
};
